void fb_init(uint64_t addr, uint32_t width, uint32_t height, uint32_t pitch, uint32_t bpp) {
    fb = (uint32_t *)addr;
    fb_w = width;
    fb_h = height;
    fb_pitch = pitch / 4;
    fb_bpp = bpp;
}

uint32_t fb_width(void) { return fb_w; }
uint32_t fb_height(void) { return fb_h; }

void fb_putpixel(uint32_t x, uint32_t y, uint32_t color) {
    if (!fb || x >= fb_w || y >= fb_h) return;
    fb[y * fb_pitch + x] = color;
}

void fb_draw_rect(uint32_t x, uint32_t y, uint32_t w, uint32_t h, uint32_t color) {
    for (uint32_t j = 0; j < h; ++j) {
        for (uint32_t i = 0; i < w; ++i) {
            fb_putpixel(x + i, y + j, color);
        }
    }
}

void fb_fill_screen(uint32_t color) {
    if (!fb) return;
    for (uint32_t y = 0; y < fb_h; ++y) {
        for (uint32_t x = 0; x < fb_w; ++x) {
            fb[y * fb_pitch + x] = color;
        }
    }
}

void fb_draw_char(uint32_t x, uint32_t y, char c, uint32_t fg, uint32_t bg) {
    if (c < 32 || c > 127) return;
    const uint8_t *glyph = font8x8[c - 32];
    for (uint32_t row = 0; row < 8; ++row) {
        uint8_t bits = glyph[row];
        for (uint32_t col = 0; col < 8; ++col) {
            uint32_t color = (bits & (1 << (7 - col))) ? fg : bg;
            fb_putpixel(x + col, y + row, color);
        }
    }
}

void fb_draw_text(uint32_t x, uint32_t y, const char *text, uint32_t fg, uint32_t bg) {
    uint32_t cx = x;
    while (*text) {
        if (*text == '\n') {
            y += 8;
            cx = x;
        } else {
            fb_draw_char(cx, y, *text, fg, bg);
            cx += 8;
        }
        ++text;
    }
}
